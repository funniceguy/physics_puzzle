// Helper function to get character by ID
export function getCharacter(id) {
    return CHARACTERS.find(char => char.id === id);
}

// Helper function to get all unlocked characters based on progress
export function getUnlockedCharacters(progress) {
    return CHARACTERS.filter(char => {
        if (char.unlockCondition.type === 'default') return true;

        if (char.unlockCondition.type === 'merge') {
            const fruitId = char.unlockCondition.fruit;
            const required = char.unlockCondition.count;
            return (progress.merges[fruitId] || 0) >= required;
        }

        if (char.unlockCondition.type === 'level') {
            const level = char.unlockCondition.level;
            const required = char.unlockCondition.count;
            return (progress.levelClears[level] || 0) >= required;
        }

        return false;
    });
}
